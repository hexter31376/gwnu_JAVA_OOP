package kor.point;

public class Point_7 {
    private int x; // x좌표
    private int y; // y좌표

    public Point_7(int x, int y){ // x와 y 입력받을때의 객체 생성자 Point_6 정의
        this.x = x;
        this.y = y;
    }

    public int get_x(){ // x좌표 반환
        return x;
    }

    public int get_y(){ // y좌표 반환
        return y;
    }
}